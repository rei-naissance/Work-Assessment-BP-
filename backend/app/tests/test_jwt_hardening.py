"""Tests for JWT hardening: iss/aud claims, validation, and SSO readiness.

Covers: tokens with correct claims pass, tokens with wrong issuer/audience
        are rejected, existing endpoints validate claims properly.
"""

import pytest
from datetime import datetime, timedelta
from unittest.mock import AsyncMock

from jose import jwt

from app.config import settings
from app.tests.conftest import make_access_token, make_refresh_token


# ---------------------------------------------------------------------------
# Access token claim validation
# ---------------------------------------------------------------------------

@pytest.mark.asyncio
async def test_access_token_includes_iss_aud_iat(client, mock_db):
    """Access tokens generated by the system include iss, aud, and iat claims."""
    token = make_access_token()
    payload = jwt.decode(
        token, settings.jwt_secret,
        algorithms=[settings.jwt_algorithm],
        audience=settings.jwt_audience,
    )

    assert payload["iss"] == settings.jwt_issuer
    assert payload["aud"] == settings.jwt_audience
    assert "iat" in payload


