@pytest.mark.asyncio
async def test_valid_token_with_correct_claims_passes(client, mock_db):
    """Token with correct iss/aud is accepted by protected endpoints."""
    token = make_access_token()
    mock_db.profiles.find_one = AsyncMock(return_value=None)

    resp = await client.get(
        "/api/profile/",
        headers={"Authorization": f"Bearer {token}"},
    )

    # Profile not found is expected (404), but NOT 401
    assert resp.status_code in (200, 404)


@pytest.mark.asyncio
async def test_token_with_wrong_issuer_rejected(client, mock_db):
    """Token signed with wrong issuer is rejected."""
    now = datetime.utcnow()
    bad_token = jwt.encode(
        {
            "sub": "aabbccddeeff001122334455",
            "email": "test@example.com",
            "is_admin": False,
            "iss": "wrong-issuer",
            "aud": settings.jwt_audience,
            "iat": now,
            "exp": now + timedelta(minutes=15),
        },
        settings.jwt_secret,
        algorithm=settings.jwt_algorithm,
    )

    resp = await client.get(
        "/api/profile/",
        headers={"Authorization": f"Bearer {bad_token}"},
    )

    assert resp.status_code == 401


